-------------------------------------------------------------------------------
-- Importing modules
-------------------------------------------------------------------------------
local Endpoint = require "endpoints.Endpoint"

-------------------------------------------------------------------------------
-- Declaring module
-------------------------------------------------------------------------------
local Info = Endpoint:new()

-------------------------------------------------------------------------------
-- Declaring instance variables
-------------------------------------------------------------------------------

-- The request method
Info.method = 'GET'

-- The request URI
Info.uri = '/'

-------------------------------------------------------------------------------
-- Returns an instance of Info class
-------------------------------------------------------------------------------
function Info:new(o)
  o = o or {}
  setmetatable(o, self)
  self.__index = self
  return o
end

return Info
